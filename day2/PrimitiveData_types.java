package day2;


public class PrimitiveData_types {
    public static void main(String args[]){
    byte byteMax = 127;
    byte byteMin = -127;

    System.out.println("Min range for byte is"+" "+byteMin+" "+"Max range for byte is"+" "+byteMax);

    short shortMax = 32767;
    short shortMin = -32768;

    System.out.println("Min range for short is"+" "+shortMin+" "+"Max range for short is"+" "+shortMax);

    int maxint = 2147483647;
    int minint = -2147483648;
    
    System.out.println("Min range for int is"+" "+minint+" "+"Max range for int is"+" "+maxint);

    }

    
}
